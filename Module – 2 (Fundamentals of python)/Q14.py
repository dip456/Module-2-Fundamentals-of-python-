""" What are negative indexes and why are they used? """

"""Ans : 

In Python, negative indexes are used to access elements from the end of a sequence, such as a string, list, or tuple.
 The index -1 refers to the last element, -2 to the second-to-last element, and so on.

For example, consider a string:
my_string = "Python"

Using positive indexes, you can access characters like this:
first_char = my_string[0]  # 'P'
second_char = my_string[1]  # 'y'

Using negative indexes, you can access characters from the end:
last_char = my_string[-1]    # 'n'
second_to_last = my_string[-2]  # 'o'
"""